class TreeFinder
  TREE = "#"

  def initialize(map)
    @map        = map
    @x_position = 1
    @tree_count = 0
  end

  def run
    split_map.each do |row|
      @x_position -= row.length if @x_position > row.length
      @tree_count += 1 if row[@x_position - 1] == TREE
      @x_position += 3
    end

    puts @tree_count
  end

  private

  def split_map
    @split_map ||= @map.split("\n")
  end
end

